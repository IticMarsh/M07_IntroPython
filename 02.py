# Demanar a l'usuari que introdueixi dos valors numèrics
valor1 = float(input("Introdueix el primer valor numèric: "))
valor2 = float(input("Introdueix el segon valor numèric: "))

# Calcular la suma dels dos valors
suma = valor1 + valor2

# Determinar quin és el valor més gran
mes_gran = max(valor1, valor2)

# Mostrar els resultats per pantalla
print(f"La suma dels dos valors és: {suma}")
print(f"El valor més gran és: {mes_gran}")
